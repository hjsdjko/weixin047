					return
				}
				if((!this.ruleForm.rexiandianhua)){
					this.$utils.msg(`热线电话不能为空`);
					return
				}
				if(this.ruleForm.rexiandianhua&&(!this.$validate.isMobile(this.ruleForm.rexiandianhua))){
					this.$utils.msg(`热线电话应输入手机格式`);
					return
				}
				if(this.ruleForm.storeupnum&&(!this.$validate.isIntNumer(this.ruleForm.storeupnum))){
					this.$utils.msg(`收藏数量应输入整数`);
					return
